package programs.P16.ArithmeticProblem;

//Write a program to demonstrate various arithmetic
public class MyMath {
    public int add(int x, int y){
        return x + y;
    }
    public int sub(int x, int y){
        return x - y;
    }
    public int mul(int x, int y){
        return x * y;
    }
    public double div(int x, int y){
        return (double) x / y;
    }
    public int mod(int x, int y){
        return x % y;
    }
}
